-- Crea le tabelle nel database corrente
DROP TABLE IF EXISTS USERS;
DROP TABLE IF EXISTS BLOCKS;
DROP TABLE IF EXISTS PAGES;

CREATE TABLE IF NOT EXISTS USERS (
   UUID_USER varchar(36) PRIMARY KEY,
   NAME varchar(50) NOT NULL,
   SURNAME varchar(50) NOT NULL,
   USERNAME varchar(20) NOT NULL,
   EMAIL varchar(20) NOT NULL,
   PASSWORD varchar(60) NOT NULL,
   ISADMIN INTEGER DEFAULT 0
);

CREATE TABLE IF NOT EXISTS BLOCKS (
   UUID_BLOCKS varchar(36) PRIMARY KEY,
   NAME varchar(50) NOT NULL,
   TYPE varchar(50) NOT NULL,
   CONTENTS varchar(50) NOT NULL,
   PAGE_BLOCKS varchar (16) NOT NULL,
   ITEM_ORDER INTEGER
);

CREATE TABLE IF NOT EXISTS PAGES (
   UUID_PAGE varchar(36) PRIMARY KEY,
   TITLE varchar(50) NOT NULL,
   AUTHOR varchar(50) NOT NULL,
   CREATED_AT varchar(20) NOT NULL,
   PUBLICATION_DATE varchar(20),
   FOREIGN KEY (AUTHOR) REFERENCES USERS(UUID_USER)
);


